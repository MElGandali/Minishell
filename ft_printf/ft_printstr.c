#include "ft_printf.h"

int	ft_printstr(char *str)
{
	int	i;
	int	cnt;

	i = 0;
	cnt = 0;
	if (!str)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (str[i] != '\0')
	{
		cnt += ft_printchar(str[i]);
		i++;
	}
	return (cnt);
}
