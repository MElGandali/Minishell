#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*p;
	size_t			i;

	i = 0;
	p = (unsigned char *)s;
	if (n == 0)
		return ;
	while (i < n)
		p[i++] = 0;
}
